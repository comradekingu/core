                  ><b-dropdown-item
                    v-for="render in supportedRenders"
                    :key="render.component"
                    @click="
                      addStep({
                        component: render.component,
                        svgGroupElement: null
                      })
                    "
                    >{{ render.description }}</b-dropdown-item
                  ></b-dropdown
                ></b-card-text
              ></b-tab
            >
          </b-tabs>
        </b-card>
      </b-col>
