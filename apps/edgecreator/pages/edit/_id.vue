      v-if="loaded"
      id="image-modal"
      scrollable
      ok-title="Select"
      :title="clickedImage"
      @ok="$root.$emit('set-option', 'xlink:href', getElementUrl(clickedImage))"
    >
      <img :alt="clickedImage" :src="getElementUrl(clickedImage)" />
    </b-modal>
  </b-container>
