<template>
  <b-container id="wrapper" fluid>
    <b-row align="center">
      <b-col class="text-left">
        <div>
          <label style="display: inline-block;width: 200px"
            >Zoom : {{ zoom }}
            <input v-model="zoom" type="range" min="1" max="8" step="0.5" />
          </label>
        </div>
      </b-col>
      <b-col>
        <b-button to="/">Home</b-button>
        <b-button @click="exportLogo">Export</b-button>
      </b-col>
      <b-col class="text-right">
        <b-form-checkbox
          v-model="showPreviousEdge"
          :disabled="!edgesBefore.length"
          >Show previous edge</b-form-checkbox
        >
        <b-form-checkbox v-model="showNextEdge" :disabled="!edgesAfter.length"
          >Show next edge</b-form-checkbox
        >
      </b-col>
    </b-row>
    <b-row v-if="loaded" align-v="start" align-h="center">
      <b-col class="text-right">
        <table class="edges">
          <tr>
            <td>
              <published-edge
                v-if="showPreviousEdge && edgesBefore.length"
                :issue-number="edgesBefore[edgesBefore.length - 1].issuenumber"
              />
            </td>
            <td>
              <svg
                ref="edge"
                :viewBox="`0 0 ${width} ${height}`"
                :width="zoom * width"
                :height="zoom * height"
                xmlns="http://www.w3.org/2000/svg"
                xmlns:xlink="http://www.w3.org/1999/xlink"
                preserveAspectRatio="none"
              >
                <g
                  v-for="(step, stepNumber) in steps"
                  :key="stepNumber"
                  :class="{
                    [step.component]: true,
                    hovered: hoveredStep === stepNumber
                  }"
                >
                  <component
                    :is="`${step.component}Render`"
                    v-if="$options.components[`${step.component}Render`]"
                    :step-number="stepNumber"
                    :svg-group="step.svgGroupElement"
                    :db-options="step.dbOptions"
                    @update="loadCurrentStepOptions"
                  ></component>
                </g>
                <rect
                  id="border"
                  :x="borderWidth / 2"
                  :y="borderWidth / 2"
                  :width="width - borderWidth"
                  :height="height - borderWidth"
                  fill="none"
                  stroke="black"
                  :stroke-width="borderWidth"
                />
              </svg>
            </td>
            <td>
              <published-edge
                v-if="showNextEdge && edgesAfter.length"
                :issue-number="edgesAfter[0].issuenumber"
              />
            </td>
          </tr>
          <tr>
            <td v-if="showPreviousEdge && edgesBefore.length">
              {{ edgesBefore[edgesBefore.length - 1].issuenumber }}
            </td>
            <td>{{ edge.numero }}<br />&#11088;</td>
            <td v-if="showNextEdge && edgesAfter.length">
              {{ edgesAfter[0].issuenumber }}
            </td>
          </tr>
        </table>
      </b-col>
      <b-col sm="10" md="8" lg="6">
        <b-card no-body>
          <b-tabs v-model="currentStepNumber" lazy pills card vertical>
            <b-tab v-for="(step, stepNumber) in steps" :key="stepNumber">
              <template v-slot:title>
                <div
                  @mouseover="hoveredStep = stepNumber"
                  @mouseleave="hoveredStep = null"
                >
                  {{ step.component }}
                </div>
              </template>
              <b-card-text v-if="step.component === 'TexteMyFonts'">
                <form-row id="text-string" label="Text">
                  <b-form-input
                    id="text-string"
                    size="sm"
                    type="text"
                    :value="currentStepOptions.text"
                    @change="$root.$emit('set-option', 'text', $event)"
                  ></b-form-input>
                </form-row>
                <form-row id="text-font" label="Font">
                  <b-form-input
                    id="text-font"
                    size="sm"
                    type="text"
                    :value="currentStepOptions.font"
                    @change="$root.$emit('set-option', 'font', $event)"
                  ></b-form-input>
                </form-row>
                <form-row id="background-color" label="Background color">
                  <b-form-input
                    id="background-color"
                    size="sm"
                    type="color"
                    :value="`#${currentStepOptions.bgColor}`"
                    @change="
                      $root.$emit(
                        'set-option',
                        'bgColor',
                        $event.replace('#', '')
                      )
                    "
                  ></b-form-input>
                </form-row>
                <form-row id="text-color" label="Text color">
                  <b-form-input
                    id="text-color"
                    size="sm"
                    type="color"
                    :value="`#${currentStepOptions.fgColor}`"
                    @change="
                      $root.$emit(
                        'set-option',
                        'fgColor',
                        $event.replace('#', '')
                      )
                    "
                  ></b-form-input>
                </form-row>
              </b-card-text>
              <b-card-text v-if="step.component === 'Remplir'">
                <form-row id="fill-color" label="Color">
                  <b-form-input
                    id="fill-color"
                    size="sm"
                    type="color"
                    :value="currentStepOptions.fill"
                    @change="$root.$emit('set-option', 'fill', $event)"
                  ></b-form-input>
                </form-row>
              </b-card-text>
              <b-card-text v-if="step.component === 'Image'">
                <form-row id="image-src" label="Image">
                  <b-form-input
                    v-if="currentStepOptions['xlink:href']"
                    id="image-src"
                    size="sm"
                    type="text"
                    readonly
                    :value="
                      currentStepOptions['xlink:href'].match(/\/([^\/]+)$/)[1]
                    "
                  ></b-form-input>
                </form-row>
                <Gallery
                  :selected-image="
                    currentStepOptions['xlink:href']
                      ? currentStepOptions['xlink:href'].match(/\/([^\/]+)$/)[1]
                      : null
                  "
                  @image-click="
                    ({ image }) => {
                      clickedImage = image
                    }
                  "
                />
              </b-card-text>
              <b-card-text v-if="step.component === 'Rectangle'">
                <form-row id="rectangle-color" label="Color">
                  <b-form-input
                    id="rectangle-color"
                    size="sm"
                    type="color"
                    :value="
                      currentStepOptions.fill === 'transparent'
                        ? currentStepOptions.stroke
                        : currentStepOptions.fill
                    "
                    @change="
                      $root.$emit(
                        'set-option',
                        currentStepOptions.fill === 'transparent'
                          ? 'stroke'
                          : 'fill',
                        $event
                      )
                    "
                  ></b-form-input>
                </form-row>
                <form-row id="is-filled" label="Filled">
                  <b-form-checkbox
                    id="is-filled"
                    :checked="currentStepOptions.fill !== 'transparent'"
                    @change="toggleFillStroke"
