<script>
import { mapMutations, mapState, mapActions } from 'vuex'
import Vue from 'vue'
import RectangleRender from '~/components/renders/RectangleRender'
import ImageRender from '~/components/renders/ImageRender'
import RemplirRender from '~/components/renders/RemplirRender'
import TexteMyFontsRender from '~/components/renders/TexteMyFontsRender'
import Gallery from '~/components/Gallery'
import FormRow from '~/components/FormRow'
import PublishedEdge from '~/components/PublishedEdge'

const parser = require('xmldom').DOMParser

export default {
  components: {
    RectangleRender,
    ImageRender,
    RemplirRender,
    TexteMyFontsRender,
    Gallery,
    FormRow,
    PublishedEdge
  },
  data() {
    return {
      loaded: false,
      borderWidth: 2,
      currentStepOptions: {},
      clickedImage: null,
      hoveredStep: null,
      showPreviousEdge: true,
      showNextEdge: true,
      supportedRenders: [
        {
          label: 'Rectangle',
          component: 'Rectangle',
          description: 'Draw a rectangle'
        },
        {
          label: 'Image',
          component: 'Image',
          description: 'Insert an image'
        },
        {
          label: 'Fill',
          component: 'Remplir',
          description: 'Fill the edge with a color'
        },
        {
          label: 'Text',
          component: 'TexteMyFontsRender',
          description: 'Insert a text'
        }
      ]
    }
  },
  computed: {
    zoom: {
      get() {
        return this.$store.state.zoom
      },
      set(value) {
        this.$store.commit('setZoom', value)
      }
    },
    currentStepNumber: {
      get() {
        return this.$store.state.currentStep.stepNumber
      },
      set(value) {
        this.$store.commit('currentStep/setStepNumber', value)
      }
    },
    ...mapState([
      'steps',
      'width',
      'height',
      'edge',
      'edgesBefore',
      'edgesAfter'
    ])
  },
  async mounted() {
    const vm = this
    const edges = await this.$axios.$get('/api/edgecreator/v2/model')
    const edge = edges.find((edge) => edge.id === parseInt(vm.$route.params.id))
    if (!edge) {
      return
    }
    this.setEdge(edge)

    this.$axios
      .$get(`/${vm.edge.numero}.svg`)
      .then((data) => {
        const doc = parser.parseFromString(data, 'image/svg+xml')
        const svgElement = doc.getElementsByTagName('svg')[0]
        vm.setDimensions({
          width: svgElement.getAttribute('width'),
          height: svgElement.getAttribute('height')
        })

        vm.loaded = true
        vm.setSteps(
          doc.getElementsByTagName('g').map((group) => ({
            component: group.getAttribute('class'),
            svgGroupElement: group
          }))
        )
      })
      .catch(() => {
        vm.$axios
          .$get(`/api/edgecreator/v2/model/${vm.edge.id}/steps`)
          .then((steps) => {
            vm.loaded = true
            const dimensions = steps.find((step) => step.ordre === -1).options
            vm.setDimensions({
              width: dimensions.Dimension_x,
              height: dimensions.Dimension_y
            })

            vm.setSteps(
              steps
                .filter((step) => step.ordre !== -1)
                .map((step) => ({
                  component: `${step.nomFonction}`,
                  dbOptions: step.options
                }))
            )
          })
      })

    this.loadGalleryItems()
    this.loadSurroundingEdges()
  },
  methods: {
    loadCurrentStepOptions(options) {
      this.currentStepOptions = options
    },
    getElementUrl(elementFileName) {
      return `${process.env.EDGES_URL}/${this.edge.country}/elements/${elementFileName}`
    },
    getEdgeUrl(issueNumber) {
      return `${process.env.EDGES_URL}/${this.edge.country}/gen/${this.edge.magazine}.${issueNumber}.png`
    },
    exportLogo() {
      const vm = this
      this.zoom = 1.5
      Vue.nextTick().then(() => {
        vm.$axios.$put('/fs/export', {
          issueNumber: vm.edge.numero,
          content: vm.$refs.edge.outerHTML
        })
      })
    },
    toggleFillStroke() {
      if (this.currentStepOptions.fill !== 'transparent') {
        this.$root.$emit('set-option', 'stroke', this.currentStepOptions.fill)
        this.$root.$emit('set-option', 'fill', 'transparent')
      } else {
        this.$root.$emit('set-option', 'fill', this.currentStepOptions.stroke)
        this.$root.$emit('set-option', 'stroke', 'transparent')
      }
    },
    test(e) {
      e.target.height = e.target.naturalHeight * this.zoom
    },
    z(e) {
      console.log(arguments)
    },
    ...mapMutations([
      'setSteps',
      'addStep',
      'setDimensions',
      'setEdge',
      'setEdgesBefore',
      'setEdgesAfter',
      'setGalleryItems'
    ]),
    ...mapActions(['loadSurroundingEdges', 'loadGalleryItems'])
  }
}
</script>
